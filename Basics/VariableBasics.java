package Basics;

public class VariableBasics {
    public static void main(String[] args) {
        int a=23;
        System.out.println(a+2);
        int b; //declaration
        b=10;  //initialization
        System.out.println(b-5);
        a=3;
        System.out.println(a);
        a=a+12;
        System.out.println(a);
        a=a-25;
        System.out.println(a);
        a=a*10;
        System.out.println(a);
        a=a/10;
        System.out.println(a);
    }
}
