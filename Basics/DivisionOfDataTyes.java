package Basics;

public class DivisionOfDataTyes {
    public static void main(String[] args) {
        System.out.println(5/2);
        System.out.println(5.0/2);
        System.out.println(5/2.0);
        System.out.println(5.0/2.0);
        System.out.println(5.00/2.0000);
        System.out.println(5.01/2.0000);
        System.out.println(5.011/2.0000);
    }
}
