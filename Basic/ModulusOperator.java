package Basic;

public class ModulusOperator {
    public static void main(String[] args) {
        System.out.println(37%5);
        System.out.println(5%37);
        System.out.println(-37%5);
        System.out.println(-5%37);
        System.out.println(37%-5);
        System.out.println(5%-37);
        System.out.println(-37%-5);
        System.out.println(-5%-37);
        
    }
}
