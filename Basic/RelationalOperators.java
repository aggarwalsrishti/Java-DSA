package Basic;

public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println(5>3);
        System.out.println(5<3);
        System.out.println(3==5);
        System.out.println(3>=5);
        System.out.println(3<=5);
        System.out.println(6*7==32);
        System.out.println(5!=3);
        boolean x= true;
        boolean y= (3>5);
        System.out.println(y);
        System.out.println(x);
    }
}
