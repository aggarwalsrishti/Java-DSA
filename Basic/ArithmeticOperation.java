package Basic;

public class ArithmeticOperation {
    public static void main(String[] args) {
        double x=50, y=8;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
